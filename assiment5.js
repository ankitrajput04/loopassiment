// Develop a program that simulates a traffic light
// using a switch statement. It should take a color (e.g., "red," "yellow,"
// "green") as input and display the appropriate message based on the color


var tarafic_light_color = "yellow"

switch(tarafic_light_color){
    case "red":
        console.log("stop")
        break;
    case "green":
        console.log("Go")
    case "yellow":
        console.log("slow down")
}